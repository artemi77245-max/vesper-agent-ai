"""
ASCII-маскот Vesper — лисёнок под звёздным небом.
Показывается при запуске агента в терминале.
"""

# ANSI-цвета для терминала (сумеречная палитра Vesper)
VIOLET = "\033[38;5;99m"   # фиолетовый — тело лисёнка
BLUE = "\033[38;5;61m"     # тёмно-синий — акценты
YELLOW = "\033[38;5;229m"  # мягкий жёлтый — звёзды
GRAY = "\033[38;5;245m"    # серый — подпись
BOLD = "\033[1m"
RESET = "\033[0m"

# Пиксель-арт лисёнок из сплошных блоков (в духе крабика Claude Code).
# Ушки сверху, глаза-пропуски, носик по центру, лапки снизу.
FOX = r"""
  ██       ██
  ███     ███
  █████████████
  ██ ██   ██ ██
  █████████████
  █████ ██ █████
    ███     ███
"""

TAGLINE = "Vesper — твой кодинг-агент. Свет в сумерках кода."


def render_mascot() -> str:
    """Собирает раскрашенного маскота с подписью для вывода в терминал."""
    fox = f"{BOLD}{VIOLET}{FOX}{RESET}"
    tagline = f"{BOLD}{VIOLET}{TAGLINE}{RESET}"
    return f"{fox}\n{tagline}"


if __name__ == "__main__":
    # Быстрый предпросмотр: python mascot.py
    print(render_mascot())
